    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'title'=>'required|string',
            'image'=>'required|image',
            'description'=>'required|string',
        ]);
        $teams = new Team;
        $teams->title = $request->title;
        $teams->description = $request->description;

        $big_file= $request->file('image');
        // dd($big_file);
        Storage::putfile('public/image/', $big_file);
        $teams->image="storage/image/".$big_file->hashName();


        $teams->save();
        return redirect()->route('admin.team.list')->with('success', 'teams successfully');
    }

